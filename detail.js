  function getProductIdFromUrl() {
    const urlParams = new URLSearchParams(window.location.search);
    return urlParams.get('id');
  }
  
  function loadProductDetail() {
    const productId = getProductIdFromUrl();
  
    if (!productId || !productDetails[productId]) {
      window.location.href = './timkiem.html';
      return;
    }
  
    const product = productDetails[productId];
  
    document.getElementById('pageTitle').textContent = product.title + ' - Công Ty TNHH Cường Hải';
    document.getElementById('productImage').src = product.image;
    document.getElementById('productImage').alt = product.title;
    document.getElementById('productTitle').textContent = product.title;
    document.getElementById('productDescription').textContent = product.description;
    document.getElementById('breadcrumbCurrent').textContent = product.title;
    document.getElementById('contentTitle').textContent = 'Giới thiệu về ' + product.title;
    document.getElementById('detailContent').innerHTML = product.content;
  
    const featuresList = document.getElementById('featuresList');
    featuresList.innerHTML = '';
    product.features.forEach(feature => {
      const li = document.createElement('li');
      li.textContent = feature;
      featuresList.appendChild(li);
    });
  }
  
  window.onload = loadProductDetail;